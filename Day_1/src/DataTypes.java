public class DataTypes {
    public static void main(String[] args) {
        // Primitive Data types. Primitive data types will start with a lower letter
        int a; // Default value is 0
        long l = 100; 
        
        double b = 5.5;
        
        float c = 5.5f; // If use float, should use f after value. If dont have 'f' java will know the value as double
        
        char d = 'a'; 
        char h; // Default value = \u0000;
        
        // References data types. References data types will start with a upper letter
        String s; // Default value is null
        String t = "hello world"; // Value = hello world
        
        int[] arrays = {1, 2, 3, 4}; // Array also is an references data types
        
        // References data types can have NULL value       
    }
}
